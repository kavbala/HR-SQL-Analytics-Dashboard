# Insights - SQL -Based HR Employee Analytics

**File:** 03_insights.md
**Description:** Business insights derived from  each of the 20
storytelling SQL queries written in simple language.

Q1. Find the list of employees along with their departments names?
Insight :
  This creates a master employee directory showing how employees are distributed across
departments.Useful for HR reporting and audits.

Q2. Identify employees who joined the company in the last 2 years?
Insight :
    Identifies recent hires,helping HR track onboarding,training needs, and hiring trends.
    
Q3. Which departments have the highest number of employees ?
Insight :
   Shows which departments are resource-heavy and may require higher budget or management 
attention.

Q4. Find employees who salary is above the average salary of their department ?
Insight :
   Highlights top performers or senior employees within each department.
   
Q5. Determine the department-wise total salary expenditure ?
Insight :
   Helps management understand cost distribution and control payroll expenses.
   
Q6. Find employees with the highest salary in each department ?
Insight :
   Identifies key or senior roles driving department costs.
   
Q7. Show department-wise average salary abd bonus ?
Insight :
   Reveals pay structure difference between departments and fairness in compensation.
   
Q8. Identify employees whose bonus is greater than 5000 ?
Insights :
     Shows highly rewarded employees,often linked to strong performance.
     
Q9. Rank employees based on their salary across the organisation ?
Insight :
   Useful for compensation benchmarking and identifying pay hierarchy.
   
Q10. Find employees in IT and HR departments and their salary details ?
Insight :
   Helps compare critical departments and their compensation patterns.
   
Q11. Identify departments where the average salary is greater than 70000 ?
Insight :
	Indicates departments with specialized or high-value skills.
    
Q12. List employees with their total compensation (salary+bonus) ?
Insight :
   Shows true employee cost to the company beyond base salary.
   
Q13. Count the number of employees per jon title ?
Insight :
  Helps identify role concentration and hiring needs.
  
Q14. Determine departments where more than 50% of employees earn above 60000 ?
Insight :
   Indicates high-paying departments and mature teams.
   
Q15. Categorize employees into salary grades: High,Medium,Low ?
Insight :
  Simplifies employees into pay bands for HR decision-making.
  
Q16. Find employees who joined before 2020 and have salary above the department average ?
Insight :
   Shows experienced employees who are still top earners,useful for retention strategy.
   
Q17. Show department-wise minimum and maximum salary ?
Insight :
   Helps identify pay gaps and salary range spread within departments.
   
Q18. Identify employees whose salary increased[assume bonus as proxy] more than 20%of their salary?
Insight :
   Highlights employees receiving performance-linked incentive.
   
Q19. List employees with department location and their salary details ?
Insight :
   Useful for location-based salary planning and cost-of-living analysis.
   
Q20. Find employees eligible for promotion based on salary(EG:those average below department average) ?
Insight :
   Identifies promotion or apprasial candidates.
   



